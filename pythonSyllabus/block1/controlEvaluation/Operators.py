# E   Exponents, then (**)
# MD  Multiplication and division, left to right, then (* /  //)
# AS  Addition and subtraction, left to right (+ -)

print ("This is the priority order, from highest to lessest: () ,", "** ,", "// ,", "%  *  / (Left to right) ,",
       "+ - (Left to right)")
